package uk.ac.ebi.tools.mztab_java;

public class MzTabParsingException extends Exception {

	public MzTabParsingException() {
		
	}

	public MzTabParsingException(String arg0) {
		super(arg0);
	}

	public MzTabParsingException(Throwable arg0) {
		super(arg0);
	}

	public MzTabParsingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
